# =========================
# + - * /
# /: trả về số thập phân
print(5 / 2)  # 2.5
# *: có thể dùng cho string "str"*n (lặp lại string với số lần n)
print("ha"*3)  # hahaha
# +: có thể dùng để nối chuỗi
print("hello" + " " + "world")  # hello world

# =========================
# //: phép chia lấy phần nguyên => trả về số nguyên
print(5 // 2)  # 2
# %: phép chia lấy phần dư => trả về số nguyên
print(5 % 2)  # 1
# **: lũy thừa 
print(4**2)  # 16
