# NOTE: trả về TRUE/ FALSE + không yêu cầu kiểu dữ liệu của 2 vế giống nhau
# =========================
# ==, !=, >, <, >=, <=
print(5 == 5)  # True
print(5 != 3)  # True

# =========================
# câu lệnh if
a = 10
if a < 0:
    print("a là số âm")
elif a == 0:
    print("a bằng 0")
else:
    print("a là số dương")